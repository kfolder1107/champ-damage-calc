/**
 * Showdown のポケデックスキー形式（小文字・スペース・ハイフン・ドット除去）に変換
 */
export function toShowdownId(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '')
}

